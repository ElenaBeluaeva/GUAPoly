# Альтернативный вариант: функция для автоматического расчета
def calculate_coordinates_circular():
    """Рассчитывает координаты по кругу"""
    coords = {}

    # Размеры клеток
    cell_width = BOARD_WIDTH // 11
    cell_height = BOARD_HEIGHT // 11

    # 0-9: нижняя сторона (справа налево)
    for i in range(10):
        x = BOARD_WIDTH - cell_width // 2 - (i * cell_width)
        y = BOARD_HEIGHT - cell_height // 2
        coords[i] = (x, y)

    # 10-19: левая сторона (снизу вверх)
    for i in range(10):
        x = cell_width // 2
        y = BOARD_HEIGHT - cell_height // 2 - (i * cell_height)
        coords[10 + i] = (x, y)

    # 20-29: верхняя сторона (слева направо)
    for i in range(10):
        x = cell_width // 2 + (i * cell_width)
        y = cell_height // 2
        coords[20 + i] = (x, y)

    # 30-39: правая сторона (сверху вниз)
    for i in range(10):
        x = BOARD_WIDTH - cell_width // 2
        y = cell_height // 2 + (i * cell_height)
        coords[30 + i] = (x, y)

    return coords

