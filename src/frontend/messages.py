def calculate_rent(cell: dict, houses: int) -> int:
    """–†–∞—Å—á–µ—Ç —Ä–µ–Ω—Ç—ã –Ω–∞ –æ—Å–Ω–æ–≤–µ –¥–æ–º–æ–≤"""
    base_rent = cell['rent']
    if houses == 0:
        return base_rent
    elif houses == 1:
        return base_rent * 5
    elif houses == 2:
        return base_rent * 15
    elif houses == 3:
        return base_rent * 45
    elif houses == 4:
        return base_rent * 80
    elif houses == 5:  # –û—Ç–µ–ª—å
        return base_rent * 125
    return base_rent


